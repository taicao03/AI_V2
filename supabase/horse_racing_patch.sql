create or replace function public.admin_horse_update_settings(
  p_session_token text,
  p_enabled boolean,
  p_min_bet integer,
  p_max_bet integer,
  p_betting_duration integer,
  p_race_duration integer,
  p_expected_version integer default null
)
returns public.horse_race_settings
language plpgsql
security definer
set search_path = public
as $$
declare
  admin_uid uuid := public.assert_admin(p_session_token);
  settings_record public.horse_race_settings%rowtype;
  updated_settings public.horse_race_settings%rowtype;
begin
  select * into settings_record
  from public.horse_race_settings
  where settings_id = 1
  for update;

  if not found then
    insert into public.horse_race_settings (settings_id)
    values (1)
    returning * into settings_record;
  end if;

  if p_expected_version is not null and settings_record.version <> p_expected_version then
    raise exception 'Horse settings changed by another admin (expected %, actual %)', p_expected_version, settings_record.version using errcode = '40001';
  end if;

  if p_min_bet < 1 or p_max_bet < p_min_bet then
    raise exception 'Invalid min/max bet settings' using errcode = '22023';
  end if;

  if p_betting_duration < 10 or p_betting_duration > 180 then
    raise exception 'Betting duration must be 10-180 seconds' using errcode = '22023';
  end if;

  if p_race_duration < 10 or p_race_duration > 180 then
    raise exception 'Race duration must be 10-180 seconds' using errcode = '22023';
  end if;

  update public.horse_race_settings
  set enabled = p_enabled,
      min_bet = p_min_bet,
      max_bet = p_max_bet,
      betting_duration = p_betting_duration,
      race_duration = p_race_duration,
      updated_by = admin_uid,
      updated_at = now(),
      version = settings_record.version + 1
  where settings_id = 1
  returning * into updated_settings;

  insert into public.admin_logs (admin_id, action, details)
  values (
    admin_uid,
    'admin_horse_update_settings',
    jsonb_build_object(
      'before', to_jsonb(settings_record),
      'after', to_jsonb(updated_settings)
    )
  );

  return updated_settings;
end;
