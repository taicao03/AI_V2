create or replace function public.horse_tick_rounds()
returns public.horse_races
language plpgsql
security definer
set search_path = public
as $$
declare
  active_race public.horse_races%rowtype;
  settings_record public.horse_race_settings%rowtype;
  winner_id uuid;
begin
  perform pg_advisory_xact_lock(hashtext('horse_racing_tick_v1'));

  select * into settings_record from public.horse_race_settings where settings_id = 1;

  if settings_record.enabled is false then
    return null;
  end if;

  select * into active_race
  from public.horse_races
  where status in ('betting', 'locked', 'racing')
  order by created_at desc
  limit 1
  for update;

  if not found then
    return public.horse_create_race(null);
  end if;

  if active_race.status = 'betting' and now() >= active_race.betting_ends_at then
    winner_id := public.horse_calculate_winner(active_race.race_id);

    update public.horse_races
    set status = 'locked',
        winner_horse_id = winner_id,
        locked_at = now(),
        lock_ends_at = now() + interval '1 second',
        race_started_at = null,
        race_ends_at = null,
        started_at = null
    where race_id = active_race.race_id
    returning * into active_race;

    return active_race;
  end if;

  if active_race.status = 'locked' and now() >= coalesce(active_race.lock_ends_at, active_race.locked_at, now()) then
    update public.horse_races
    set status = 'racing',
        race_started_at = now(),
        race_ends_at = now() + make_interval(secs => settings_record.race_duration),
        started_at = now()
    where race_id = active_race.race_id
    returning * into active_race;

    return active_race;
  end if;

  if active_race.status = 'racing' and now() >= coalesce(active_race.race_ends_at, now()) then
    active_race := public.horse_settle_race(active_race.race_id, null);
    perform public.horse_create_race(null);
    return active_race;
  end if;

  return active_race;
end;
$$;

create or replace function public.horse_get_public_state()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  settings_record public.horse_race_settings%rowtype;
  active_race public.horse_races%rowtype;
  recent_races jsonb;
  top_bettors jsonb;
  winners jsonb;
begin
  select * into settings_record from public.horse_race_settings where settings_id = 1;

  if not found then
    insert into public.horse_race_settings (settings_id) values (1) returning * into settings_record;
  end if;

  if settings_record.enabled then
    begin
      active_race := public.horse_create_next_race();
    exception
      when others then
        select * into active_race
        from public.horse_races
        where status in ('betting', 'locked', 'racing')
        order by created_at desc
        limit 1;
    end;
  else
    select * into active_race
    from public.horse_races
    where status in ('betting', 'locked', 'racing')
    order by created_at desc
    limit 1;
  end if;

  select coalesce(jsonb_agg(to_jsonb(r) order by r.created_at desc), '[]'::jsonb)
  into recent_races
  from (
    select hr.*
    from public.horse_races hr
    order by hr.created_at desc
    limit 20
  ) r;

  select coalesce(jsonb_agg(to_jsonb(b) order by b.total_amount desc), '[]'::jsonb)
  into top_bettors
  from (
    select
      hb.user_id,
      u.account_name,
      u.display_name,
      u.avatar_url,
      sum(hb.bet_amount)::bigint as total_amount
    from public.horse_bets hb
    join public.users u on u.uid = hb.user_id
    where hb.created_at >= now() - interval '24 hours'
    group by hb.user_id, u.account_name, u.display_name, u.avatar_url
    order by total_amount desc
    limit 10
  ) b;

  select coalesce(jsonb_agg(to_jsonb(w) order by w.ended_at desc), '[]'::jsonb)
  into winners
  from (
    select
      hr.race_id,
      hr.race_number,
      hr.winner_horse_id,
      h.name as winner_name,
      h.avatar as winner_avatar,
      hr.ended_at
    from public.horse_races hr
    left join public.horses h on h.horse_id = hr.winner_horse_id
    where hr.status = 'completed'
    order by hr.ended_at desc nulls last
    limit 10
  ) w;

  return jsonb_build_object(
    'settings', to_jsonb(settings_record),
    'horses', (
      select coalesce(jsonb_agg(to_jsonb(h) order by h.sort_order asc, h.created_at asc), '[]'::jsonb)
      from public.horses h
      where h.enabled = true
    ),
    'active_race', case when active_race.race_id is null then null else to_jsonb(active_race) end,
    'recent_races', recent_races,
    'recent_winners', winners,
    'top_bettors', top_bettors
  );
end;
$$;

