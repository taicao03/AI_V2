create or replace function public.horse_create_race(p_created_by uuid default null)
returns public.horse_races
language plpgsql
security definer
set search_path = public
as $$
declare
  settings_record public.horse_race_settings%rowtype;
  created_race public.horse_races%rowtype;
  snapshot jsonb;
begin
  select * into settings_record from public.horse_race_settings where settings_id = 1 for update;

  if not found then
    insert into public.horse_race_settings (settings_id) values (1)
    returning * into settings_record;
  end if;

  if settings_record.enabled is false then
    raise exception 'Horse racing is disabled' using errcode = '22023';
  end if;

  perform public.horse_validate_horses_config();
  snapshot := public.horse_get_settings_snapshot();

  insert into public.horse_races (
    status,
    winner_horse_id,
    horses_snapshot,
    settings_version,
    betting_started_at,
    betting_ends_at,
    created_by
  )
  values (
    'betting',
    null,
    snapshot->'horses',
    settings_record.version,
    now(),
    now() + make_interval(secs => settings_record.betting_duration),
    p_created_by
  )
  returning * into created_race;

  return created_race;
end;
$$;

create or replace function public.horse_calculate_winner(p_race_id uuid)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  race_record public.horse_races%rowtype;
  horse_item record;
  total_weight numeric(18,6) := 0;
  random_point numeric(18,6);
  cumulative numeric(18,6) := 0;
  picked_horse_id uuid;
begin
  select * into race_record
  from public.horse_races
  where race_id = p_race_id
  for update;

  if not found then
    raise exception 'Race not found' using errcode = 'P0002';
  end if;

  for horse_item in
    select
      (h.value->>'horse_id')::uuid as horse_id,
      coalesce((h.value->>'win_probability')::numeric, 0) as win_probability
    from jsonb_array_elements(race_record.horses_snapshot) h
  loop
    total_weight := total_weight + greatest(horse_item.win_probability, 0);
  end loop;

  if total_weight <= 0 then
    raise exception 'No valid horse probability in race snapshot' using errcode = '22023';
  end if;

  random_point := random() * total_weight;

  for horse_item in
    select
      (h.value->>'horse_id')::uuid as horse_id,
      coalesce((h.value->>'win_probability')::numeric, 0) as win_probability
    from jsonb_array_elements(race_record.horses_snapshot) h
    order by coalesce((h.value->>'sort_order')::integer, 0) asc
  loop
    cumulative := cumulative + greatest(horse_item.win_probability, 0);

    if random_point <= cumulative then
      picked_horse_id := horse_item.horse_id;
      exit;
    end if;
  end loop;

  if picked_horse_id is null then
    select (h.value->>'horse_id')::uuid
    into picked_horse_id
    from jsonb_array_elements(race_record.horses_snapshot) h
    limit 1;
  end if;

  return picked_horse_id;
end;
$$;

create or replace function public.horse_refresh_player_stats(p_user_id uuid)
returns public.horse_player_stats
language plpgsql
security definer
set search_path = public
as $$
declare
  stats_record public.horse_player_stats%rowtype;
  account text;
  dname text;
  avatar text;
  longest_streak integer := 0;
  current_streak integer := 0;
  streak_count integer := 0;
  bet_row record;
begin
  select u.account_name, u.display_name, u.avatar_url
  into account, dname, avatar
  from public.users u
  where u.uid = p_user_id;

  for bet_row in
    select hb.result
    from public.horse_bets hb
    where hb.user_id = p_user_id
      and hb.status = 'settled'
    order by hb.settled_at asc nulls last, hb.created_at asc
  loop
    if bet_row.result = 'win' then
      streak_count := streak_count + 1;
      longest_streak := greatest(longest_streak, streak_count);
    else
      streak_count := 0;
    end if;
  end loop;

  streak_count := 0;
  for bet_row in
    select hb.result
    from public.horse_bets hb
    where hb.user_id = p_user_id
      and hb.status = 'settled'
    order by hb.settled_at desc nulls last, hb.created_at desc
  loop
    if bet_row.result = 'win' then
      streak_count := streak_count + 1;
    else
      exit;
    end if;
  end loop;
  current_streak := streak_count;

  insert into public.horse_player_stats (
    user_id,
    account_name,
    display_name,
    avatar_url,
    total_winnings,
    biggest_win,
    total_bets,
    win_count,
    best_streak,
    current_streak,
    updated_at
  )
  select
    p_user_id,
    coalesce(account, 'player'),
    coalesce(dname, 'Player'),
    avatar,
    coalesce(sum(case when hb.status = 'settled' and hb.result = 'win' then hb.points_change else 0 end), 0)::bigint,
    coalesce(max(case when hb.status = 'settled' and hb.result = 'win' then hb.points_change else 0 end), 0)::integer,
    coalesce(count(*) filter (where hb.status = 'settled'), 0)::integer,
    coalesce(count(*) filter (where hb.status = 'settled' and hb.result = 'win'), 0)::integer,
    longest_streak,
    current_streak,
    now()
  from public.horse_bets hb
  where hb.user_id = p_user_id
  on conflict (user_id) do update
    set account_name = excluded.account_name,
        display_name = excluded.display_name,
        avatar_url = excluded.avatar_url,
        total_winnings = excluded.total_winnings,
        biggest_win = excluded.biggest_win,
        total_bets = excluded.total_bets,
        win_count = excluded.win_count,
        best_streak = excluded.best_streak,
        current_streak = excluded.current_streak,
        updated_at = excluded.updated_at
  returning * into stats_record;

  return stats_record;
end;
$$;
create or replace function public.horse_settle_race(p_race_id uuid, p_admin_id uuid default null)
returns public.horse_races
language plpgsql
security definer
set search_path = public
as $$
declare
  race_record public.horse_races%rowtype;
  bet_record public.horse_bets%rowtype;
  user_record public.users%rowtype;
  is_win boolean;
  payout_amount integer;
  delta integer;
  next_points integer;
  next_locked integer;
begin
  select * into race_record
  from public.horse_races
  where race_id = p_race_id
  for update;

  if not found then
    raise exception 'Race not found' using errcode = 'P0002';
  end if;

  if race_record.status = 'completed' then
    return race_record;
  end if;

  if race_record.winner_horse_id is null then
    raise exception 'Winner is not selected' using errcode = '22023';
  end if;

  for bet_record in
    select *
    from public.horse_bets hb
    where hb.race_id = race_record.race_id
      and hb.status = 'pending'
    order by hb.created_at asc
    for update
  loop
    select * into user_record
    from public.users u
    where u.uid = bet_record.user_id
    for update;

    is_win := bet_record.horse_id = race_record.winner_horse_id;
    payout_amount := case when is_win then floor(bet_record.bet_amount::numeric * bet_record.odds_multiplier)::integer else 0 end;
    delta := case when is_win then payout_amount else -bet_record.bet_amount end;

    next_points := user_record.points + delta;
    next_locked := greatest(0, user_record.locked_points - bet_record.bet_amount);

    update public.users
    set points = next_points,
        locked_points = next_locked,
        total_bets = total_bets + 1,
        total_wins = total_wins + case when is_win then 1 else 0 end,
        total_losses = total_losses + case when is_win then 0 else 1 end,
        total_points_won = total_points_won + case when is_win then delta else 0 end,
        total_points_lost = total_points_lost + case when is_win then 0 else bet_record.bet_amount end,
        points_updated_at = now()
    where uid = user_record.uid;

    update public.horse_bets
    set status = 'settled',
        result = case when is_win then 'win' else 'lose' end,
        payout = payout_amount,
        points_before = user_record.points,
        points_after = next_points,
        points_change = delta,
        settled_at = now()
    where bet_id = bet_record.bet_id;

    insert into public.points_transactions (
      user_id,
      game_type,
      type,
      amount,
      points_before,
      points_after,
      locked_before,
      locked_after,
      bet_id,
      note,
      created_at
    )
    values (
      user_record.uid,
      'horse_racing',
      case when is_win then 'horse_bet_win' else 'horse_bet_loss' end,
      delta,
      user_record.points,
      next_points,
      user_record.locked_points,
      next_locked,
      null,
      case
        when is_win then 'Horse race win settlement; horse_bet_id=' || bet_record.bet_id::text
        else 'Horse race loss settlement; horse_bet_id=' || bet_record.bet_id::text
      end,
      now()
    );

    if is_win then
      insert into public.horse_chat_messages (
        user_id,
        display_name,
        avatar,
        role,
        vip_level,
        text,
        is_system,
        created_at
      )
      values (
        user_record.uid,
        'System',
        null,
        'system',
        0,
        user_record.display_name || ' just won with ' || coalesce((select h.name from public.horses h where h.horse_id = race_record.winner_horse_id), 'Unknown Horse') || ' 🔥',
        true,
        now()
      );
    end if;

    perform public.horse_refresh_player_stats(user_record.uid);
  end loop;

  update public.horse_races
  set status = 'completed',
      ended_at = now(),
      race_ends_at = coalesce(race_ends_at, now())
  where race_id = race_record.race_id
  returning * into race_record;

  if race_record.winner_horse_id is not null then
    perform public.horse_refresh_rarity(race_record.winner_horse_id);
  end if;

  return race_record;
end;
$$;

