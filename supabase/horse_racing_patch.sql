create or replace function public.horse_place_bet(
  p_session_token text,
  p_horse_id uuid,
  p_bet_amount integer
)
returns public.horse_bets
language plpgsql
security definer
set search_path = public
as $$
declare
  current_uid uuid := public.get_session_user_id(p_session_token);
  profile public.users%rowtype;
  settings_record public.horse_race_settings%rowtype;
  active_race public.horse_races%rowtype;
  horse_record public.horses%rowtype;
  existing_bet public.horse_bets%rowtype;
  updated_bet public.horse_bets%rowtype;
  locked_before integer;
begin
  perform public.horse_tick_rounds();

  select * into settings_record
  from public.horse_race_settings
  where settings_id = 1;

  if settings_record.enabled is false then
    raise exception 'Horse racing is disabled' using errcode = '22023';
  end if;

  if p_bet_amount is null or p_bet_amount < settings_record.min_bet or p_bet_amount > settings_record.max_bet then
    raise exception 'Bet amount must be between % and %', settings_record.min_bet, settings_record.max_bet using errcode = '22023';
  end if;

  select * into active_race
  from public.horse_races hr
  where hr.status = 'betting'
  order by hr.created_at desc
  limit 1
  for update;

  if not found then
    active_race := public.horse_create_next_race();
    select * into active_race
    from public.horse_races hr
    where hr.race_id = active_race.race_id
    for update;
  end if;

  if active_race.status <> 'betting' or now() >= active_race.betting_ends_at then
    raise exception 'Betting is closed for this race' using errcode = '22023';
  end if;

  select * into horse_record
  from public.horses h
  where h.horse_id = p_horse_id
    and h.enabled = true;

  if not found then
    raise exception 'Horse is not available for betting' using errcode = '22023';
  end if;

  if horse_record.odds_multiplier < 1 then
    raise exception 'Horse odds multiplier is invalid (< 1), please contact admin' using errcode = '22023';
  end if;

  select * into profile
  from public.users u
  where u.uid = current_uid
  for update;

  if not found then
    raise exception 'User profile not found' using errcode = 'P0002';
  end if;

  if profile.is_banned then
    raise exception 'Account is banned' using errcode = '42501';
  end if;

  if profile.points - profile.locked_points < p_bet_amount then
    raise exception 'Not enough available points' using errcode = '22023';
  end if;

  locked_before := profile.locked_points;

  select * into existing_bet
  from public.horse_bets hb
  where hb.race_id = active_race.race_id
    and hb.user_id = current_uid
    and hb.horse_id = p_horse_id
    and hb.status = 'pending'
  for update;

  if found then
    update public.horse_bets
    set bet_amount = existing_bet.bet_amount + p_bet_amount,
        odds_multiplier = horse_record.odds_multiplier
    where bet_id = existing_bet.bet_id
    returning * into updated_bet;
  else
    insert into public.horse_bets (
      race_id,
      user_id,
      horse_id,
      bet_amount,
      odds_multiplier,
      status,
      result,
      payout,
      points_change
    )
    values (
      active_race.race_id,
      current_uid,
      p_horse_id,
      p_bet_amount,
      horse_record.odds_multiplier,
      'pending',
      null,
      0,
      0
    )
    returning * into updated_bet;
  end if;

  update public.users
  set locked_points = public.users.locked_points + p_bet_amount,
      points_updated_at = now()
  where uid = current_uid
  returning * into profile;

  insert into public.points_transactions (
    user_id,
    game_type,
    type,
    amount,
    points_before,
    points_after,
    locked_before,
    locked_after,
    bet_id,
    note,
    created_at
  )
  values (
    current_uid,
    'horse_racing',
    'horse_bet_lock',
    p_bet_amount,
    profile.points,
    profile.points,
    locked_before,
    profile.locked_points,
    null,
    'Horse race bet locked; horse_bet_id=' || updated_bet.bet_id::text,
    now()
  );

  return updated_bet;
end;
$$;
