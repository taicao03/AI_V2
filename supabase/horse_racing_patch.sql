create or replace function public.horse_validate_horses_config()
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  enabled_count integer;
  total_probability numeric(14,4);
begin
  select count(*), coalesce(sum(h.win_probability), 0)
  into enabled_count, total_probability
  from public.horses h
  where h.enabled = true;

  if enabled_count < 5 or enabled_count > 10 then
    raise exception 'Horse racing requires 5-10 enabled horses, found %', enabled_count using errcode = '22023';
  end if;

  if abs(total_probability - 100) > 0.0001 then
    raise exception 'Enabled horse probabilities must total 100%%, got %', total_probability using errcode = '22023';
  end if;
end;
$$;

create or replace function public.horse_rarity_from_wins(p_wins integer)
returns text
language plpgsql
immutable
as $$
begin
  if coalesce(p_wins, 0) >= 30 then
    return 'legendary';
  end if;
  if coalesce(p_wins, 0) >= 15 then
    return 'epic';
  end if;
  if coalesce(p_wins, 0) >= 5 then
    return 'rare';
  end if;
  return 'common';
end;
$$;

create or replace function public.horse_refresh_rarity(p_horse_id uuid)
returns public.horses
language plpgsql
security definer
set search_path = public
as $$
declare
  wins_count integer := 0;
  horse_record public.horses%rowtype;
begin
  select count(*)::integer
  into wins_count
  from public.horse_races hr
  where hr.status = 'completed'
    and hr.winner_horse_id = p_horse_id;

  update public.horses
  set rarity = public.horse_rarity_from_wins(wins_count)
  where horse_id = p_horse_id
  returning * into horse_record;

  return horse_record;
end;
$$;

do $$
declare
  horse_row record;
begin
  for horse_row in
    select h.horse_id
    from public.horses h
  loop
    perform public.horse_refresh_rarity(horse_row.horse_id);
  end loop;
end $$;

