create or replace function public.admin_horse_upsert_horse(
  p_session_token text,
  p_horse_id uuid,
  p_name text,
  p_avatar text,
  p_speed_rating integer,
  p_rarity text,
  p_odds_multiplier numeric,
  p_win_probability numeric,
  p_enabled boolean,
  p_sort_order integer
)
returns public.horses
language plpgsql
security definer
set search_path = public
as $$
declare
  admin_uid uuid := public.assert_admin(p_session_token);
  normalized_name text := trim(coalesce(p_name, ''));
  horse_record public.horses%rowtype;
  before_snapshot jsonb;
begin
  if normalized_name = '' then
    raise exception 'Horse name is required' using errcode = '22023';
  end if;

  if p_speed_rating < 1 or p_speed_rating > 100 then
    raise exception 'Speed rating must be 1-100' using errcode = '22023';
  end if;

  if p_odds_multiplier < 1 then
    raise exception 'Odds multiplier must be >= 1' using errcode = '22023';
  end if;

  if p_win_probability < 0 then
    raise exception 'Probability must be >= 0' using errcode = '22023';
  end if;

  if p_horse_id is not null then
    select to_jsonb(h) into before_snapshot
    from public.horses h
    where h.horse_id = p_horse_id;

    update public.horses
    set name = normalized_name,
        avatar = nullif(trim(coalesce(p_avatar, '')), ''),
        speed_rating = p_speed_rating,
        odds_multiplier = p_odds_multiplier,
        win_probability = p_win_probability,
        enabled = p_enabled,
        sort_order = coalesce(p_sort_order, 0)
    where horse_id = p_horse_id
    returning * into horse_record;

    if not found then
      raise exception 'Horse not found' using errcode = 'P0002';
    end if;
  else
    insert into public.horses (
      name,
      avatar,
      speed_rating,
      rarity,
      odds_multiplier,
      win_probability,
      enabled,
      sort_order
    )
    values (
      normalized_name,
      nullif(trim(coalesce(p_avatar, '')), ''),
      p_speed_rating,
      'common',
      p_odds_multiplier,
      p_win_probability,
      p_enabled,
      coalesce(p_sort_order, 0)
    )
    returning * into horse_record;

    before_snapshot := null;
  end if;

  horse_record := public.horse_refresh_rarity(horse_record.horse_id);
  perform public.horse_validate_horses_config();

  insert into public.admin_logs (admin_id, action, details)
  values (
    admin_uid,
    'admin_horse_upsert_horse',
    jsonb_build_object(
      'before', before_snapshot,
      'after', to_jsonb(horse_record)
    )
  );

  return horse_record;
end;
$$;

